/*
--------------------------------------------------------------
1Ô∏è‚É£ ENCAPSULATION
--------------------------------------------------------------

Wrapping data + functions into a single unit (class)

Achieved by:
‚Ä¢ Private data members
‚Ä¢ Public getter/setter

Ensures:
‚úî Data security
‚úî Controlled access
*/

class BankAccount {
private:
    int balance;

public:
    void setBalance(int amt) {
        balance = amt;
    }

    int getBalance() {
        return balance;
    }
};

/*
--------------------------------------------------------------
2Ô∏è‚É£ ABSTRACTION
--------------------------------------------------------------

Hiding implementation details
Showing only essential features

Example:
ATM ‚Üí You know withdraw(), not internal logic

Achieved using:
‚Ä¢ Abstract class
‚Ä¢ Pure virtual function
*/

class Shape {
public:
    virtual void draw() = 0;   // Pure Virtual Function
};

class Circle : public Shape {
public:
    void draw() {
        cout << "Drawing Circle" << endl;
    }
};

/*
--------------------------------------------------------------
3Ô∏è‚É£ INHERITANCE
--------------------------------------------------------------

One class acquires properties of another class

Syntax:
class Child : accessSpecifier Parent
*/

class Animal {
public:
    void eat() {
        cout << "Eating..." << endl;
    }
};

class Dog : public Animal {
public:
    void bark() {
        cout << "Barking..." << endl;
    }
};

/*
Types of Inheritance:
1) Single
2) Multiple
3) Multilevel
4) Hierarchical
5) Hybrid
*/

/*
--------------------------------------------------------------
4Ô∏è‚É£ POLYMORPHISM
--------------------------------------------------------------

One name ‚Üí Multiple forms

Types:
1) Compile-time (Overloading)
2) Run-time (Overriding)
*/

/* Function Overloading */
class Calculator {
public:
    int add(int a, int b) {
        return a + b;
    }

    int add(int a, int b, int c) {
        return a + b + c;
    }
};

/* Runtime Polymorphism */
class Base {
public:
    virtual void show() {
        cout << "Base class show()" << endl;
    }
};

class Derived : public Base {
public:
    void show() override {
        cout << "Derived class show()" << endl;
    }
};

