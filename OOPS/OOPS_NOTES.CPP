int main() {

    // Object Creation
    Student s;
    s.roll = 1;
    s.name = "Yash";
    s.display();

    // Encapsulation
    BankAccount acc;
    acc.setBalance(10000);
    cout << "Balance: " << acc.getBalance() << endl;

    // Abstraction
    Shape* sp = new Circle();
    sp->draw();

    // Inheritance
    Dog d;
    d.eat();
    d.bark();

    // Compile-time Polymorphism
    Calculator calc;
    cout << calc.add(2, 3) << endl;
    cout << calc.add(2, 3, 4) << endl;

    // Runtime Polymorphism
    Base* obj = new Derived();
    obj->show();

    // Constructor & Destructor
    Demo d1;
    Sample s1;

    return 0;
}

